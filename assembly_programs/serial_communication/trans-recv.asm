;TRANS AND RECV AT SAME TIME
ORG 00H
SJMP MAIN

ORG 23H
JB RI,RECV
MOV A,@R0
MOV SBUF,A
BK:JNB TI,BK
INC R0
CLR TI
RETI


RECV:
MOV A,SBUF
MOV @R1,A
INC R1
CLR RI
RETI

MAIN:
MOV R0,#40H
MOV R1,#50H
MOV IE,#82H
MOV TMOD,#22H
MOV TH0,#-6
MOV TH1,#-6
MOV SCON,#50H
SETB TR0
SETB TR1

LOOP:
SJMP LOOP
HLT:SJMP HLT
